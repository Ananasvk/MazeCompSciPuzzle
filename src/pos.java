public class pos
{
    private int col, row;

    public pos(int col, int row)
    {
        this.col = col;
        this.row = row;
    }

    public int getCol()
    {
        return col;
    }

    public int getRow()
    {
        return row;
    }

    public void setCol(int col)
    {
        this.col = col;
    }
    public void setRow(int row)
    {
        this.row = row;
    }
}
